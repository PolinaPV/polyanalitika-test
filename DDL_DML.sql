	id VARCHAR(12) NOT NULL,
	name VARCHAR(512),
	price INT NOT NULL,
	sdate DATE NOT NULL,
	edate DATE NOT NULL,
	is_actual VARCHAR(12)
);
-- DROP TABLE IF EXISTS podsipanina.salesman;
CREATE TABLE podsipanina.salesman (
	id INT NOT NULL,
	fio VARCHAR(512),
	department_id INT NOT NULL
);
-- DROP TABLE IF EXISTS podsipanina.department;
CREATE TABLE podsipanina.department (
	filial_id INT NOT NULL,
	department_id INT NOT NULL,
	dep_chif_id INT NOT NULL
