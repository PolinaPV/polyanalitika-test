CREATE SCHEMA IF NOT EXISTS podsipanina;
-- DROP TABLE IF EXISTS podsipanina.sales;
CREATE TABLE podsipanina.sales (
	sale_date DATE NOT NULL,
	salesman_id INT NOT NULL,
	item_id VARCHAR(12) NOT NULL,
	quantity INT NOT NULL,
	final_price INT NOT NULL
);
-- DROP TABLE IF EXISTS podsipanina.product;
CREATE TABLE podsipanina.product (
	id VARCHAR(12) NOT NULL,
	name VARCHAR(512),
	price INT NOT NULL,
	sdate DATE NOT NULL,
	edate DATE NOT NULL,
	is_actual VARCHAR(12)
);
-- DROP TABLE IF EXISTS podsipanina.service;
